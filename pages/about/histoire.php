            <p>C’est à ce moment que nous avons eu l’idée de créer un site qui met en relation les pilotes et les sponsors, on a voulu simplifier ça en disant “le Tinder des pilotes”. 
                Grâce à Stand for, nous avons pu nous faire des contacts et surtout en apprendre davantage sur ce domaine. 
                Pour FirstLap, notre ambition est de créer une plateforme sur laquelle les pilotes peuvent rechercher des sponsors et vice versa. Les utilisateurs, qu'ils soient pilotes ou sponsors, auront la possibilité de créer un profil détaillé intégrant leurs critères et informations pertinentes. À partir de ces critères, notre système de matchmaking automatique mettra en relation les pilotes avec les sponsors, simplifiant ainsi le processus de recherche de partenariat. Ce mécanisme facilitera non seulement la mise en relation, mais permettra également aux utilisateurs d'obtenir les coordonnées nécessaires pour initier le contact et formaliser les partenariats.</p>
